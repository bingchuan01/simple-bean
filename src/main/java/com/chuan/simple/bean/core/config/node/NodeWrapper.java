package com.chuan.simple.bean.core.config.node;

import com.chuan.simple.helper.resource.Resource;

public class NodeWrapper {

    private Resource resource;

    private Node node;

    public NodeWrapper(Node node, Resource resource) {
        this.node = node;
        this.resource = resource;
    }

    public Resource getResource() {
        return resource;
    }

    public void setResource(Resource resource) {
        this.resource = resource;
    }

    public Node getNode() {
        return node;
    }

    public void setNode(Node node) {
        this.node = node;
    }

    @Override
    public String toString() {
        return "resource:\n" + this.resource.toString() + "\nnode:\n"
                + this.node.toString();
    }

}
